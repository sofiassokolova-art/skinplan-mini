// –ò–°–ü–†–ê–í–õ–ï–ù–û: Dev-–≤–∞–ª–∏–¥–∞—Ü–∏—è —Ü–µ–ø–æ—á–∫–∏ –∏–Ω—Ñ–æ-—ç–∫—Ä–∞–Ω–æ–≤ –¥–ª—è –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–µ–Ω–∏—è —Ü–∏–∫–ª–æ–≤ –∏ –æ—à–∏–±–æ–∫
if (process.env.NODE_ENV === 'development') {
  const validateInfoScreens = () => {
    const screenIds = new Set(INFO_SCREENS.map(s => s.id));
    const errors: string[] = [];
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —É–Ω–∏–∫–∞–ª—å–Ω–æ—Å—Ç—å id
    if (screenIds.size !== INFO_SCREENS.length) {
      errors.push('‚ùå –î—É–±–ª–∏–∫–∞—Ç—ã id –≤ INFO_SCREENS');
    }
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –≤—Å–µ showAfterInfoScreenId —É–∫–∞–∑—ã–≤–∞—é—Ç –Ω–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ —ç–∫—Ä–∞–Ω—ã
    for (const screen of INFO_SCREENS) {
      if (screen.showAfterInfoScreenId && !screenIds.has(screen.showAfterInfoScreenId)) {
        errors.push(`‚ùå –≠–∫—Ä–∞–Ω "${screen.id}" —Å—Å—ã–ª–∞–µ—Ç—Å—è –Ω–∞ –Ω–µ—Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π showAfterInfoScreenId: "${screen.showAfterInfoScreenId}"`);
      }
    }
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ü–∏–∫–ª—ã –≤ —Ü–µ–ø–æ—á–∫–µ
    const visited = new Set<string>();
    const recursionStack = new Set<string>();
    
    const checkCycle = (screenId: string): boolean => {
      if (recursionStack.has(screenId)) {
        errors.push(`‚ùå –û–±–Ω–∞—Ä—É–∂–µ–Ω —Ü–∏–∫–ª –≤ —Ü–µ–ø–æ—á–∫–µ –∏–Ω—Ñ–æ-—ç–∫—Ä–∞–Ω–æ–≤, –≤–∫–ª—é—á–∞—é—â–∏–π: ${screenId}`);
        return true;
      }
      if (visited.has(screenId)) {
        return false;
      }
      
      visited.add(screenId);
      recursionStack.add(screenId);
      
      const screen = INFO_SCREENS.find(s => s.id === screenId);
      if (screen?.showAfterInfoScreenId) {
        if (checkCycle(screen.showAfterInfoScreenId)) {
          return true;
        }
      }
      
      recursionStack.delete(screenId);
      return false;
    };
    
    for (const screen of INFO_SCREENS) {
      if (screen.showAfterInfoScreenId && !visited.has(screen.id)) {
        checkCycle(screen.id);
      }
    }
    
    if (errors.length > 0) {
      console.error('üö® –û—à–∏–±–∫–∏ –≤–∞–ª–∏–¥–∞—Ü–∏–∏ INFO_SCREENS:', errors);
    } else {
      console.log('‚úÖ INFO_SCREENS –≤–∞–ª–∏–¥–∞—Ü–∏—è –ø—Ä–æ–π–¥–µ–Ω–∞');
    }
  };
  
  // –ó–∞–ø—É—Å–∫–∞–µ–º –≤–∞–ª–∏–¥–∞—Ü–∏—é –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –º–æ–¥—É–ª—è
  validateInfoScreens();
}
