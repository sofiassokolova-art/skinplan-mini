async function seedProductsExtended() {
  console.log('üå± Seeding extended products database...');

  // –°–æ–∑–¥–∞–µ–º –±—Ä–µ–Ω–¥—ã
  const brandMap = new Map<string, number>();
  
  for (const brandData of BRANDS) {
    const brand = await prisma.brand.upsert({
      where: { name: brandData.name },
      update: { country: brandData.country },
      create: {
        name: brandData.name,
        country: brandData.country,
        isActive: true,
      },
    });
    brandMap.set(brandData.name, brand.id);
    console.log(`  ‚úÖ Brand: ${brandData.name}`);
  }

  // –°–æ–∑–¥–∞–µ–º –ø—Ä–æ–¥—É–∫—Ç—ã
  let createdCount = 0;
  let updatedCount = 0;

  for (const productData of PRODUCTS) {
    const brandId = brandMap.get(productData.brand);
    if (!brandId) {
      console.error(`  ‚ùå Brand not found: ${productData.brand}`);
      continue;
    }

    const existing = await prisma.product.findFirst({
      where: {
        brandId,
        name: productData.name,
      },
    });

    const productPayload = {
      brandId,
      name: productData.name,
      line: null,
      category: productData.category,
      step: productData.step,
      skinTypes: productData.skinTypes,
      concerns: productData.concerns,
      isFragranceFree: productData.isFragranceFree,
      isNonComedogenic: productData.isNonComedogenic,
      priceSegment: productData.priceSegment,
      price: productData.price,
      descriptionUser: productData.descriptionUser,
      marketLinks: productData.marketLinks as any,
      status: 'published' as const,
    };

    if (existing) {
      await prisma.product.update({
        where: { id: existing.id },
        data: productPayload,
      });
      updatedCount++;
      console.log(`  üîÑ Updated: ${productData.brand} - ${productData.name}`);
    } else {
      await prisma.product.create({
        data: productPayload,
      });
      createdCount++;
      console.log(`  ‚úÖ Created: ${productData.brand} - ${productData.name}`);
    }
  }

  console.log(`\n‚úÖ Products seeding completed!`);
  console.log(`   Created: ${createdCount}`);
  console.log(`   Updated: ${updatedCount}`);
  console.log(`   Total: ${PRODUCTS.length}`);
}

seedProductsExtended()
  .catch((e) => {
    console.error('‚ùå Error seeding:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });

