// (один и тот же день → один и тот же совет, без «мерцания» при перезаходе).

export type TipPhase = 'adaptation' | 'building' | 'active' | 'results';

function phaseOf(day: number): TipPhase {
  if (day <= 7) return 'adaptation';
  if (day <= 14) return 'building';
  if (day <= 21) return 'active';
  return 'results';
}

