function normalizeSkinType(skinType?: string | null): keyof typeof SKIN_TYPE_TIPS {
  const s = (skinType || '').toLowerCase();
  if (/сух|dry/.test(s)) return 'dry';
  if (/жир|oil/.test(s)) return 'oily';
  if (/комб|combin/.test(s)) return 'combination';
  if (/чувств|sensit/.test(s)) return 'sensitive';
  return 'normal';
}

function matchConcernKeys(concerns: string[]): Array<keyof typeof CONCERN_TIPS> {
  const joined = concerns.join(' ').toLowerCase();
  const keys: Array<keyof typeof CONCERN_TIPS> = [];
  if (/акне|высып|acne|pimple/.test(joined)) keys.push('acne');
  if (/пигмент|пятн|pigment|spot|melasma/.test(joined)) keys.push('pigmentation');
  if (/розац|краснот|купероз|rosacea|redness/.test(joined)) keys.push('rosacea');
  if (/обезвож|сухост|dehydr|dryness/.test(joined)) keys.push('dehydration');
  if (/возраст|морщ|старен|aging|wrinkle|anti-?age/.test(joined)) keys.push('aging');
  if (/пор|чёрны|черны|comedon|blackhead|pore/.test(joined)) keys.push('pores');
  return keys;
}

/**
 * Подбирает совет дня под профиль. Детерминирован по дню: один и тот же день
 * всегда даёт один и тот же совет, но соседние дни различаются.
 */
export function generateDailyTip(params: {
  day: number;
  skinType?: string | null;
  concerns?: string[];
}): string {
  const day = Number.isFinite(params.day) && params.day > 0 ? Math.floor(params.day) : 1;
  const concerns = Array.isArray(params.concerns) ? params.concerns : [];

  const candidates: string[] = [...PHASE_TIPS[phaseOf(day)]];
  candidates.push(...(SKIN_TYPE_TIPS[normalizeSkinType(params.skinType)] || []));
  for (const key of matchConcernKeys(concerns)) {
    candidates.push(...CONCERN_TIPS[key]);
  }

  if (candidates.length === 0) return PHASE_TIPS.adaptation[0];

  // Детерминированный «разброс» по дню, чтобы соседние дни не повторялись.
  const idx = (day * 7 + day) % candidates.length;
  return candidates[idx];
}
