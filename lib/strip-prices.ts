// lib/strip-prices.ts
// Политика: клиентский мини-апп НИКОГДА не должен получать цену продукта.
// `price` хранится в БД только для серверной budget-логики (подбор по бюджету),
// но не должен уходить на клиент (цены в магазинах отличаются — см. MEMORY).
//
// План — большой вложенный объект (products[], plan28.days[], ProductCard, ...),
// и price вплетён в несколько внутренних структур. Чтобы не охотиться за каждым
// полем по отдельности (и не пропустить новое), вырезаем все ключи `price`
// рекурсивно на границе сериализации ответа.
export function stripPrices<T>(data: T): T {
  return JSON.parse(
    JSON.stringify(data, (key, value) => (key === 'price' ? undefined : value)),
  ) as T;
}
