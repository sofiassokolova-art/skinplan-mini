// public/sw.js
// KILL-SWITCH Service Worker.
//
// Предыдущая версия SW кэшировала статические чанки по `response.status === 200`,
// но обрезанный чанк (плохой DPI/VPN-канал отдаёт неполное тело со статусом 200)
// тоже попадал в кэш и затем отдавался offline-first при ЛЮБОЙ сети → приложение
// застревало на «не удалось загрузить» и само не чинилось (React-компонент очистки
// не успевал смонтироваться на битых чанках).
//
// Этот SW не кэширует и не перехватывает запросы. Его единственная задача —
// вычистить старые кэши, снять собственную регистрацию и перезагрузить открытые
// вкладки, чтобы они пошли в сеть за свежими ассетами. Браузер периодически
// перекачивает sw.js (он мелкий, доезжает даже на плохом канале) и подхватит эту
// версию у застрявших пользователей.

self.addEventListener('install', () => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    (async () => {
      // Берём контроль над уже открытыми вкладками, которыми управлял старый SW.
      await self.clients.claim();

      // Удаляем все кэши (включая закэшированные обрезанные чанки).
      const cacheKeys = await caches.keys();
      await Promise.all(cacheKeys.map((key) => caches.delete(key)));

      // Снимаем собственную регистрацию — больше ничего не перехватываем.
      await self.registration.unregister();

      // Перезагружаем открытые вкладки, чтобы они подтянули свежие ассеты из сети.
      const windowClients = await self.clients.matchAll({ type: 'window' });
      for (const client of windowClients) {
        client.navigate(client.url);
      }
    })()
  );
});

// Намеренно НЕТ обработчика 'fetch': запросы идут напрямую в сеть, ничего не кэшируется.
